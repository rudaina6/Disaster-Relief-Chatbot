CREATE TABLE droughts (
    drought_id INT PRIMARY KEY,
    location TEXT,
    origin TEXT,
    associated_types TEXT,
    start_date DATE,
    end_date DATE,
    total_deaths INT,
    no_affected INT,
    total_affected INT
);
CREATE TABLE earthquakes (
    earthquake_id INT PRIMARY KEY,
    location TEXT,
    magnitude FLOAT,
    magnitude_scale VARCHAR(50),
    latitude FLOAT,
    longitude FLOAT,
    start_date DATE,
    end_date DATE,
    total_deaths INT,
    no_injured FLOAT,
    no_affected FLOAT,
    no_homeless FLOAT,
    total_affected FLOAT,
    reconstruction_costs_usd FLOAT,
    total_damage_usd FLOAT
);
CREATE TABLE epidemics (
    epidemic_id INT PRIMARY KEY,
    epidemic_cause VARCHAR(255),
    event_name VARCHAR(255),
    location VARCHAR(255),
    magnitude_scale VARCHAR(255),
    start_date DATE,
    end_date DATE,
    total_deaths INT,
    total_affected INT
);
CREATE TABLE floods (
    flood_id INT PRIMARY KEY,
    location TEXT,
    origin TEXT,
    start_date DATE,
    end_date DATE,
    total_deaths INT,
    no_injured INT,
    no_affected INT,
    no_homeless INT,
    total_affected INT,
    reconstruction_costs INT,
    total_damage INT
);

CREATE TABLE helplines (
    name VARCHAR(255),
    region VARCHAR(50),
    phone_number VARCHAR(50)
);
CREATE TABLE shelters (
    sheter_id INT PRIMARY KEY AUTO_INCREMENT,
     province VARCHAR(50),
    city VARCHAR(50),
    address_location TEXT,
    contact_number VARCHAR(15),
    type VARCHAR(50),
    capacity INT
);
CREATE TABLE hospitals (
    hospital_id INT PRIMARY KEY AUTO_INCREMENT,
    province VARCHAR(100),
    district VARCHAR(100),
    name VARCHAR(255),
    type VARCHAR(50),
    address TEXT,
    occupancy int,
    phone_number varchar(20)
);
